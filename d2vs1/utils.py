def pass_ordinal_to_letter(pass_number):
    if pass_number == "First":
        pass_number = "A"
    elif pass_number == "Second":
        pass_number = "B"
    elif pass_number == "Third":
        pass_number = "C"
    elif pass_number == "Fourth":
        pass_number = "D"
    elif pass_number == "Fifth":
        pass_number = "E"

    return pass_number
    
    